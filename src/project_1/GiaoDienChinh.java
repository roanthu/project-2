    private int inArrCanh(int a, int b){
        for(int i = 0; i < graph.data.getArrCanh().size(); i++){
            int A = graph.data.getArrCanh().get(i).getIndexPointA();
            int B = graph.data.getArrCanh().get(i).getIndexPointB();
            if((a == A && b == B) || (a == B)&&(b == A)) return i;
        }
        return -1;
    }
