    private int findIndex(String index){
        for(int i = 0; i < graph.data.getArrNut().size(); i++){
            if(graph.data.getArrNut().get(i).getIndex().equalsIgnoreCase(index))
                return i;
        }
        JOptionPane.showMessageDialog(null, "Không có đỉnh "+index);
        return -1;
    }
